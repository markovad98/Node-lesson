const os = require('os');

// Платформа
console.log(os.platform());
// Архитектура
console.log(os.arch());
// Информация
console.log(os.cpus());
// Свободная память
console.log(os.freemem());
// Всего памяти
console.log(os.totalmem());
// Корневая директория
console.log(os.homedir());
// Время работы системы
console.log(os.uptime());

